require 'test_helper'

class PedidosControllerTest < ActionController::TestCase
  setup do
    @pedido = pedidos(:one)
  end

  test "should get index" do
    get :index
    assert_response :success
    assert_not_nil assigns(:pedidos)
  end

  test "should get new" do
    get :new
    assert_response :success
  end

  test "should create pedido" do
    assert_difference('Pedido.count') do
      post :create, pedido: { estado_ped_cod: @pedido.estado_ped_cod, guia_desp_ped_cod: @pedido.guia_desp_ped_cod, ped_fecha: @pedido.ped_fecha, pro_cod: @pedido.pro_cod }
    end

    assert_redirected_to pedido_path(assigns(:pedido))
  end

  test "should show pedido" do
    get :show, id: @pedido
    assert_response :success
  end

  test "should get edit" do
    get :edit, id: @pedido
    assert_response :success
  end

  test "should update pedido" do
    patch :update, id: @pedido, pedido: { estado_ped_cod: @pedido.estado_ped_cod, guia_desp_ped_cod: @pedido.guia_desp_ped_cod, ped_fecha: @pedido.ped_fecha, pro_cod: @pedido.pro_cod }
    assert_redirected_to pedido_path(assigns(:pedido))
  end

  test "should destroy pedido" do
    assert_difference('Pedido.count', -1) do
      delete :destroy, id: @pedido
    end

    assert_redirected_to pedidos_path
  end
end
